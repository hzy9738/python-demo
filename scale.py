# 进制
print(0b11)      # 二进制

print(0o10)      # 八进制

print(0x10)      # 十六进制

#转换成二进制
print(bin(0b11))
print(bin(0o10))
print(bin(0x10))

#转换成八进制
print(hex(0b11))
print(hex(0o10))
print(hex(0x10))

#转换成十进制
print(int(0b11))
print(int(0o10))
print(int(0x10))

#转换成十六进制
print(oct(0b11))
print(oct(0o10))
print(oct(0x10))
